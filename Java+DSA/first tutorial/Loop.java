public class Loop {
    public static void main(String[] args) {
        System.out.println("Start");
        
        //While loop
        int count = 1;
        while(count!=5){
            System.out.println(count);
            count++;
        }

        for(int count2=1;count2!=5;count2++){
            System.out.println(count2);
        }
    }
}